<h1>What is PiePHP?</h1>
<h2>It's fast!</h2>
<p>PiePHP is an open source framework for rapid application development in PHP.  It follows the Model View Controller pattern, and it's intended to be the fastest framework in three key areas:</p>

<blockquote>

	<h3>Development time</h3>
	<p>PiePHP includes tools that make web development easier than ever. Our Refresher is ideal for cross-browser testing of layouts.  Our error handling makes debugging easier than ever.  And our scaffolds allow extensible user interfaces to be built instantly.</p>

	<h3>Server-side rendering</h3>
	<p>PiePHP uses lazy-loading techniques that dramatically reduce page load time.  It can serve twice as many requests per second as other major PHP frameworks when serving cached pages, and the difference is even greater for many types of non-cached pages.</p>

	<h3>Client-side rendering</h3>
	<p>PiePHP is tackling client-side rendering issues that other frameworks simply ignore.  PiePHP will minify and GZip your HTML, CSS and JavaScript to make every request as small as possible. It groups CSS or JavaScript files together to minimize requests. It also features an AJAX-enabled persistent page, so browsers load new content without reloading entire pages.</p>

</blockquote>
